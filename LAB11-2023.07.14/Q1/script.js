function performMathOperations() {
    // Assign values to variables x and y
    var x = 6;
    var y = 4;

    // Calculate the sum
    var sum = x + y;

    // Calculate the difference
    var difference = x - y;

    // Calculate the product
    var product = x * y;

    // Calculate the division
    var division = x / y;

    // Display the results in the specified format
    var output = "Sum : " + sum + "\n";
    output += "Difference : " + difference + "\n";
    output += "Product : " + product + "\n";
    output += "Division : " + division;

    // Display the output in an alert box
    window.alert(output);
}
